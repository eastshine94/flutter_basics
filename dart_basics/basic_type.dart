void main() {
  // 정수
  int num1 = 1;
  int num2 = 2;
  print(num1 + num2);
  print(num1 - num2);
  print(num1 / num2);
  print(num1 * num2);

  //실수
  double d1 = 2.5;
  double d2 = 1.3;
  print(d1 + d2);
  print(d1 - d2);
  print(d1 * d2);
  print(d1 / d2);

  // boolean
  bool isTrue = true;
  bool isFalse = false;
  print(isTrue);
  print(isFalse);

  // string
  String s1 = "안녕";
  String s2 = "월드";
  print(s1 + " " + s2);
  print('${s1} ${s2}');
  print('$s1 $s2');
}
